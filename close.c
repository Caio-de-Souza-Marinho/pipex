#include "pipex.h"

void	free_pipex(t_pipex *pipex)
{
	free_split(pipex->cmd1_args);
	free_split(pipex->cmd2_args);
	free(pipex->cmd1_path);
	free(pipex->cmd2_path);
	free(pipex);
}

void	free_split(char **arr)
{
	int	i;

	i = 0;
	while (arr[i])
	{
		free(arr[i]);
		i++;
	}
	free(arr);
}
